package hw4JavaVariables;
 

/*Naming convention of variables -> Variables name start with lower case and follow camel case or snake case feature.
 * String -> Is the type of variable
 * int -> represents integer
 * char ->represents character, For example 'M' as in male
 * boolean ->represents type of variable with two possible values, For example true or false. 
 */
public class MySelf {

	public String myCityString;
	public String myName = "Rubayyi Saaim";
	public int myAge = 15;
	public char mySex = 'F';
	public boolean fullTimeWorker = false;
	public char myGradeInEnglish = 'A';

}
